export function getRegionNames(language: 'uz' | 'ru' = 'ru'): string[] {
  return UZBEKISTAN_REGIONS.map(r => language === 'uz' ? r.nameUz : r.name);
}

export function getDistrictsByRegion(regionName: string): string[] {
  const region = UZBEKISTAN_REGIONS.find(r => r.name === regionName || r.nameUz === regionName);
  return region?.districts || [];
}
